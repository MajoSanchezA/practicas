A diferencia del bucle **FOR**, que se repite un número predefinido de veces, el bucle WHILE no tiene un número fijo de iteraciones.
Continuará ejecutándose mientras la condición evaluada sea verdadera. Es importante tener cuidado al usar WHILE para evitar caer en un bucle infinito,
donde la condición nunca se vuelve falsa y la repetición no termina.*/

